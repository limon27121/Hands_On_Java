package OOP_for_Testers.methods_and_constructors;

public class Method {

    //no parameter and no return type
      void m1(){
          System.out.println("hello");
      }

    //no parameter and  return data
      String m2(){
          return "my boy";
      }

    // parameter and no return
      void m3(String param){
          System.out.println(param);
      }


    //param and return
    int m4(int num){
          return num;
    }

}
